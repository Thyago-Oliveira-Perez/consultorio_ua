  <div id="container">
    <h1 class="title is-2" style="color: #ffff">Detalhes de Especialidades</h1>
    <div id="cadastro">
      <div id="input-field">
        <label>Status</label>
        <p v-if="especialidade.ativo">Habilitado</p>
        <p v-if="!especialidade.ativo">Desabilitado</p>
      </div>
      <div id="input-field">
        <label>Nome</label>
        <p>{{ especialidade.nome }}</p>
      </div>
    </div>
  </div>
  <button class="button is-link" style="background-color: #42b983">
    <router-link to="/especialidades">Voltar</router-link>
  </button>
</template>

